namespace Faactory.Channels.Buffers;

/// <summary>
/// Represents the endianness of a buffer
/// </summary>
public enum Endianness
{
    /// <summary>
    /// Big endian byte order (most significant byte first)
    /// </summary>
    BigEndian,

    /// <summary>
    /// Little endian byte order (least significant byte first)
    /// </summary>
    LittleEndian
}
